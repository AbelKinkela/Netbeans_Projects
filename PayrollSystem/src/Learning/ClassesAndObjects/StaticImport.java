package Learning.ClassesAndObjects;

import static java.lang.Math.*; //import all (*) static methods in class Math

/**
 *
 * @author Marcelo
 */
public class StaticImport {
    public static void main(String[] args) {
        System.out.printf("sqrt(900): %.2f\n", sqrt(900));
        System.out.printf("ceil(-3.6): %.2f\n", ceil(-3.6));
        System.out.printf("log(E) : %.2f\n", log(E));
        System.out.printf("cos(0.0): %.2f\n", cos(0.0));
        System.out.printf("PI: %.2f\n", PI);
        

    }
}
